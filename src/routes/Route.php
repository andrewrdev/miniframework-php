<?php

namespace src\routes;

class Route
{
    /*
    |------------------------------------------------------------------------------------------------------------------
    | Constructor
    |------------------------------------------------------------------------------------------------------------------
    */

    public function __construct()
    {
        $this->run();
    }

    /*
    |------------------------------------------------------------------------------------------------------------------
    | Routes
    |------------------------------------------------------------------------------------------------------------------
    */

    public function routes()
    {
        $routes['index'] = array(
            'route' => '/',
            'controller' => 'IndexController',
            'action' => 'index'
        );

        $routes['sobre'] = array(
            'route' => '/sobre',
            'controller' => 'IndexController',
            'action' => 'sobre'
        );

        return $routes;
    }

    /*
    |------------------------------------------------------------------------------------------------------------------
    | Get URL
    |------------------------------------------------------------------------------------------------------------------
    */

    public function getUrl()
    {
        if (!isset($_GET['url'])) {
            $_GET['url'] = '/';
        }

        return parse_url(strtolower($_GET['url']), PHP_URL_PATH);
    }

    /*
    |------------------------------------------------------------------------------------------------------------------
    | Run
    |------------------------------------------------------------------------------------------------------------------
    */

    public function run()
    {
        foreach ($this->routes() as $route) {
            if ($this->getUrl() == $route['route']) {
                $class = 'src\\controllers\\' . ucfirst($route['controller']);
                $action = $route['action'];
                $controller = new $class;
                $controller->$action();
            }
        }
    }
}
